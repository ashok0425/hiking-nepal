        <h2 class="text-success">Altitude Information</h2>
        <p>What is Altitude?</p>
        <p>Altitude is known as elevation, and it is often used to refer to the height above sea level. Altitude is related
            to air pressure. Trekkers and mountaineers can measure their altitude by measuring the air pressure around them.
            As the altitude rises, air pressure drops. In other words, if the indicated altitude is high, the air pressure
            becomes low. The human body reacts to high altitudes. Decreased air pressure means that less oxygen is available
            for breathing. One normal effect of altitude is shortness of breath since the lungs have to work harder to
            deliver oxygen to the bloodstream. It can take days and even weeks for a body to adjust to high altitude and low
            air pressure. To prevent severe altitude sickness, mountaineers bring supplemental (extra) supplies of oxygen
            and limit their time in the 'high elevation'.
            Acclimatization We are experts at operating adventure trips at high-altitude destinations. Every trip we run has
            a realistic acclimatization program. Our treks are designed to allow gradual height gain, spread over several
            days. There is no way of predicting who will suffer from altitude but, for the vast majority of people, a slow
            ascent to height will produce minimal effects. Acclimatizing takes time and there are no safe shortcuts. All
            trips to altitudes over 3,000 meters carry medicines essential to the treatment of altitude illness, and our
            leaders are trained to administer them and to know the symptoms</p>

